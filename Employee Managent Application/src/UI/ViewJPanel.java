    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = tblEmployeeDrc.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please Select a Row to delete");

        }else{

        DefaultTableModel model = (DefaultTableModel) tblEmployeeDrc.getModel();
        PersonDetails selectedPerson = (PersonDetails) model.getValueAt(selectedRowIndex,0);

        employeeDetails.deleteEmployee(selectedPerson);

        JOptionPane.showMessageDialog(this, "Employee Deleted");

        populateTable();
        
        txtName.setText("");
        txtEmployeeId.setText("");
        txtAge.setText("");
        txtGender.setText("");
        txtStartDate.setText("");
        txtLevel.setText("");
        txtTeamInfo.setText("");
        txtPositionTitle.setText("");
        txtCellPhoneNo.setText("");
        txtEmailId.setText("");
        txtPhotoLocation.setText("");
        lblImage.setIcon(null); 
        
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
