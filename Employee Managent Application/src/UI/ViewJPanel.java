  private void populateTable(){
      
      DefaultTableModel model = (DefaultTableModel) tblEmployeeDrc.getModel();
      model.setRowCount(0);
      
      for(PersonDetails pd : employeeDetails.getEmployeeDetails()){
      
          Object[] row = new Object[11];
          row[0]=pd;
          row[1]=pd.getEmployee_Id();
          row[2]=pd.getAge();
          row[3]=pd.getGender();
          row[4]=pd.getStartDate();
          row[5]=pd.getLevel();
          row[6]=pd.getTeamInfo();
          row[7]=pd.getPositionTitle();
          row[8]=pd.getCellPhoneNo();
          row[9]=pd.getEmailAddress();
          row[10]=pd.getPhotoLocation();
      
          model.addRow(row);
      }
              
    }

